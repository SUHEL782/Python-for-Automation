# Bitwise operation
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
print("Bitwise AND: ", num1 & num2)
print("Bitwise OR: ", num1 | num2)
print("Bitwise XOR: ", num1 ^ num2)
print("Bitwise NOT of first number: ", ~num1)
print("Bitwise NOT of second number: ", ~num2)
print("Left Shift of first number: ", num1 << 1)
print("Left Shift of second number: ", num2 << 1)
print("Right Shift of first number: ", num1 >> 1)
print("Right Shift of second number: ", num2 >> 1)

num = 5

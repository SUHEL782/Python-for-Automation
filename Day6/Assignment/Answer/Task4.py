# Assignment Operators
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
print ("1 assignment: ", num1 == num2)
print ("2 assignment: ", num1 != num2)
print ("3 assignment: ", num1 > num2)
print ("4 assignment: ", num1 < num2)
print ("5 assignment: ", num1 >= num2)
print ("6 assignment: ", num1 <= num2)
    
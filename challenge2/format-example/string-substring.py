# subString method
substing = "Hello, this is a sample string."
print("Original string:", substing)
substing2=substing[2:5]
print("Substring from index 2 to 5:", substing2)  # Extracts characters from index 2 to 5
# Extracting a substring from the string
substring1 = substing[0:5]
print("Extracted substring:", substring1)  # Extracts characters from index 0 to 5